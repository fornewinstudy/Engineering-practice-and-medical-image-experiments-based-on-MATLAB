%(1)
% 各点坐标
x1 = [0;0];
x2 = [1;0];
x3 = [1;1];
x4 = [0;1];
% 原矩阵
x = [x1,x2,x3,x4,x1];
% 变换矩阵
A = [1 0.5;0 1];
% 左乘
y = A * x;
% 绘画原矩阵
plot(x(1,:),x(2,:), 'b')
hold on % 保留绘图
% 绘画结果矩阵
plot(y(1,:),y(2,:),'r')
axis([0 1.5 0 1.5]) %对坐标轴进行标定
grid on % 显示
%(2)
% 变换矩阵
B = [0.8 -0.9;0.5 0.55];
% 原矩阵左乘变换矩阵
z = B * y;
% 绘画原矩阵
plot(y(1,:),y(2,:), 'b')
hold on % 保留绘图
% 绘画结果矩阵
plot(z(1,:),z(2,:),'r')
axis([-1 1.5 0 1.5 ]) %对坐标轴进行标定
grid on % 显示