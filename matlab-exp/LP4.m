%2(2)
% 目标函数
fun2 = @(x) (x(1) - 9/4)^2 + (x(2) - 2)^2;
% nonlcon方法定义不等式约束系数
A4 = [1, 1 ; -1, 0 ; 0, -1];
% nonlcon方法定义不等式约束常数
b4 = [6; 0; 0];
% 定义初始点
x0 = [0; 0];
% 变量下界
lb4 = [0; 0];
% 变量上界
ub4 = [];
% 非线性等式约束
nonlcon = @(x) deal([x(1)^2 - x(2)], []);
% 使用nonlcon求解非线性规划问题
[x4, fval4] = fmincon(fun2, x0, A4, b4, [], [], [], [], nonlcon);
% 显示结果
disp('问题4的最优解:')
disp(x4)
disp('问题4的最优值:')
disp(fval4)