% 设计好的陷波器系数
b1 = -1.41280200320219;
b2 = 0.999001876938208;
a1 = -1.41280200320219;
a2 = 0.998003753876415;

b = [0.999001876938208, b1, b2];
a = [1, a1, a2];

% 输入信号
n = 0:99;
x = 3*cos(0.25*pi*n); % 仅为示例，根据实际情况替换成你的输入信号

% 初始条件
y = zeros(size(n));

% 差分方程的迭代计算
for i = 3:length(n)
    y(i) = b(1)*x(i) + b(2)*x(i-1) + b(3)*x(i-2) - a(2)*y(i-1) - a(3)*y(i-2);
end

% 绘制输入信号和滤波器输出
figure;
subplot(2,1,1);
stem(n, x);
title('输入信号');
xlabel('n');
ylabel('幅度');

subplot(2,1,2);
stem(n, y);
title('滤波器输出');
xlabel('n');
ylabel('幅度');
